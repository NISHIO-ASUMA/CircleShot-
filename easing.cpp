CEasing::CEasing()
{

}
//===============================
// デストラクタ
//===============================
CEasing::~CEasing()
{

}
//===============================
// イージングの加速
//===============================
float CEasing::EaseInsine(float t)
{
	return 1 - cos((t * D3DX_PI) / 2);
}
//===============================
// イージングの減速
//===============================
float CEasing::EaseOutsine(float t)
{
	return sin((t * D3DX_PI) / 2);
}